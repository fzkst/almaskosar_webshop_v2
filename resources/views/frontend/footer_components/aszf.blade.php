        <div class="mt-5">
            <h6 class="fw-bold" id="szavatossag">A vásárlástól való elállás / felmondás (fogyasztói szerződés esetén)</h6>
            <p>
                Hibás teljesítés (Ptk. 6:157. §)
                A kötelezett hibásan teljesít, ha a szolgáltatás a teljesítés időpontjában nem felel meg a szerződésben,
                vagy jogszabályban megállapított minőségi követelményeknek. Nem teljesít hibásan a kötelezett, ha a jogosult
                a hibát a szerződéskötés időpontjában ismerte, vagy ismernie kellett. Fogyasztó esetében az ellenkező
                bizonyításig vélelmezni kell, hogy a teljesítést követő hat hónapon belül a fogyasztó által felismert hiba
                már a teljesítés időpontjában megvolt, kivéve, ha e vélelem a dolog természetével, vagy a hiba jellegével
                összeegyeztethetetlen.
                Kellékszavatossági, termékszavatossági és jótállási igény nem érvényesíthető párhuzamosan a termék ugyanazon
                hibája miatt.
            </p>
            <p>
                Kellékszavatosság
                Olyan szerződés alapján, ahol a felek kölcsönös szolgáltatásokkal tartoznak, a kötelezett a hibás
                teljesítésért kellékszavatossággal tartozik.
                A kellékszavatossági jogok érvényesítésére a Ptk. 6:159. §-ában foglalt rendelkezések irányadóak. A
                jogosultat a következő jogok illetik meg:
                a) választása szerint kijavítást vagy kicserélést követelhet, kivéve kivéve, ha a választott
                kellékszavatossági jog teljesítése lehetetlen, vagy ha az a kötelezettnek - másik kellékszavatossági igény
                teljesítésével összehasonlítva - aránytalan többletköltséget eredményezne, figyelembe véve a szolgáltatás
                hibátlan állapotban képviselt értékét, a szerződésszegés súlyát és a kellékszavatossági jog teljesítésével a
                jogosultnak okozott érdeksérelmet; vagy

                b) az ellenszolgáltatás arányos leszállítását igényelheti, a hibát a kötelezett költségére kijavíttathatja,
                vagy a szerződéstől elállhat, ha a kötelezett a kijavítást, vagy a kicserélést nem vállalta, vagy e
                kötelezettségének megfelelő határidőben, a a jogosult érdekeit kímélve nem tud eleget tenni, vagy ha a
                jogosultnak a kijavításhoz vagy kicseréléshez fűződő érdeke megszűnt. Jelentéktelen hiba miatt elállásnak
                nincs helye.

                A jogosult a választott kellékszavatossági jogról másikra térhet át. Az áttéréssel okozott költséget köteles
                a kötelezettnek megfizetni, kivéve, ha az áttérésre a kötelezett adott okot, vagy az áttérés egyébként
                indokolt volt.
            </p>
            <p>
                Kellékszavatossági jogokat az a Vásárló is érvényesíthet, aki nem minősül fogyasztónak.
            </p>
            <p>
                Ha a fogyasztó szavatossági igényt kíván érvényesíteni az Eladóval szemben, a fogyasztói szerződés
                megkötését a fogyasztónak kell bizonyítania. Ha az Eladó a szerződésmegkötését vitatja, köteles felhívni a
                fogyasztó figyelmét a panasztétel lehetőségére és a panasz intézésének a fogyasztóvédelmi törvényben
                foglaltakkal összhangban álló módjára. A fogyasztói szerződés megkötését bizonyítottnak kell tekinteni, ha a
                fogyasztó bemutatja az ellenérték megfizetését igazoló bizonylatot (az ÁFA törvény alapján kibocsátott
                számlát vagy nyugtát).
            </p>
            <p>
                Fogyasztónak nem minősülő Vásárló kellékszavatossági igénye a teljesítés időpontjától számított egy év alatt
                évül el. Fogyasztó és vállalkozás közötti szerződés esetén a fogyasztó kellékszavatossági igénye a
                teljesítés időpontjától számított két év alatt évül el. Ha a fogyasztó és a vállalkozás közötti szerződés
                tárgya használt dolog, a felek rövidebb elévülési időben is megállapodhatnak; egy évnél rövidebb elévülési
                határidő ebben az esetben sem köthető ki érvényesen.
                Nem számít bele az elévülési időbe a kijavítási időnek az a része, amely alatt a jogosult a dolgot
                rendeltetésszerűen nem tudja használni.

                A dolognak a kicseréléssel vagy a kijavítással érintett részére a kellékszavatossági igény elévülése újból
                kezdődik. Ezt a szabályt kell alkalmazni arra az esetre is, ha a kijavítás következményeként új hiba
                keletkezik.

                A határidők számítása a teljesítés időpontjától kezdődik. Az igényérvényesítési határidő azt jelenti, hogy
                milyen határidővel kell a jogosultnak a szavatossági igény érvényesítésére irányuló keresetlevelet a
                hatáskörrel és illetékességgel rendelkező bíróságnál benyújtania.
            </p>
            <p>
                A jogosult a hiba felfedezése után késedelem nélkül köteles a hibát az Eladóval közölni. Fogyasztó esetén a
                hiba felfedezésétől számított két hónapon belül közölt hibát késedelem nélkül közöltnek kell tekinteni. A
                közlés késedelméből eredő kárért a jogosult felelős.
                A szavatossági igényt a szolgáltatott dolog minden olyan hibája miatt határidőben érvényesítettnek kell
                tekinteni, amely a megjelölt hibát előidézte. Ha a jogosult a szavatossági igényét a dolognak - a megjelölt
                hiba szempontjából - elkülöníthető része tekintetében érvényesíti, a szavatossági igény a dolog egyéb
                részeire nem minősül érvényesítettnek.
            </p>
            <p>
                A szavatossági kötelezettség teljesítésével kapcsolatos költségek az Eladót terhelik. Ha a dolog
                meghibásodásában a jogosultat terhelő karbantartási kötelezettség elmulasztása is közrehatott, a
